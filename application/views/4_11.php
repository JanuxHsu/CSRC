        <div style="font-size: 24px; font-weight: bold; ">開學交通意外高峰期 軍訓室提醒新生注意安全</div>
        <div style="font-size: small; ">校園生活‧2012-09-14‧學生事務處</div>
        <p></p>
        <div style="text-align: right; ">
          <a href="http://www.nccu.edu.tw/news/detail.php?news_id=4549">http://www.nccu.edu.tw/news/detail.php?news_id=4549</a>
        </div>
        <div>
          <p style="text-align: center; ">
            <img src="http://osa2.nccu.edu.tw/~military/csrc/new_d/illustrations/advocacy_101/tn_main.jpg">
          </p>
          <div style="text-align: center; font-weight: bold; ">
            警政署交通組科長張夢麟宣導交通安全常識。<br>照片提供：軍訓室。
          </div>
          <p></p>
          <p>
            【軍訓室記者李怡蓉報導】學期初是交通意外事件高峰危險期，學務處軍訓室配合「超政新生定位創意營」，14日邀請警政署交通組科長張夢麟演講，宣導交通安全常識，提醒同學們小心。
          </p>
          <p>
            張夢麟先播放因未繫安全帶而釀成車禍傷亡的真實案例影片，讓現場學生驚覺嚴重性，另一方面，他也以英國交通安全宣導影片《<a href="http://www.youtube.com/watch?v=h-8PBx7isoM">Embrace Life– always wear your seatbelt</a>》，從溫馨角度提醒大家，繫上安全帶不只是個人行為，也是對周遭親友負責的表現。
          </p>
          <p>
            交通安全意外肇事案件中，摩托車占了60%，張夢麟同時藉由影片指出許多機車騎士在道路上的危險行徑，提醒學生騎乘摩托車要確實遵守交通規則，保持安全行車距離，留意自身與他人的行車動向。
          </p>
          <p>
            新聞一蔡佩瑜對影片印象深刻，警惕自己要遵守交通規則，無論駕駛或乘客都要繫安全帶，禍害發生時才可將傷害減至最輕。
          </p>
          <p>
            軍訓室提醒同學，政大附近包括羹大王前路口、政大麥側、道南橋路段、恆光橋、木柵及秀明路口等路段，都是易肇事路段，行經時務必多注意自身安全，騎乘機車的同學更要注意周遭來車，以免發生意外。
          </p>
