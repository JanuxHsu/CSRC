        <dt>
            作品名稱
        </dt>
        <dd>
            上路前的視力檢查表
        </dd>
        <dt>
            創作說明
        </dt>
        <dd>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人往往注重眼前享樂，忽略了背後所產生的危險。以喝酒為例，喝酒前，氣氛歡愉盛情難卻；黃湯下肚酒後駕車可能造成車禍甚至死亡。
            <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以此為鑑，我決定做一張「視力檢查表」，並放上「上路前，你看的到死亡嗎？」的標語，呼籲駕駛人在開車上路前，仔細思考、把眼光放遠一點。視力檢查表上面兩排都是酒類食品，象徵著「喝酒」，最下面一排有骷髗頭和車禍，象徵「死亡和危險」。希望駕駛人在享樂前，不只能看到美酒，更可以看到事後的車禍、危險和死亡而有所警覺。
            <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以「黑、白」作為主色調，搭配「皺摺的紙」為材質，象徵肅穆、莊嚴，作為對駕駛人的警示海報用途，以收警告之效。
        </dd>
    </dl>
