            酒駕，沒有出口。
        </dd>
        <dt>
            創作說明
        </dt>
        <dd>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;酒後駕車的刑罰雖已加重，但許多人仍貪圖一時享樂與方便，罔顧自己甚至他人的安全，造成一輩子的遺憾。
            <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以拒絕酒駕為創作核心理念，酒後駕車正如走入一座沒有出口的迷宮，找不到正確的方向，更迷失了自己，而從瓶中流溢出的酒，則象徵著酒駕肇事所付出的血的代價。背景以黑白為主色調，聚焦在倒落的酒瓶，增強拒絕酒駕的意象。
        </dd>
