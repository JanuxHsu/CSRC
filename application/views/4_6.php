        <dd>
            在行車安全中，除了一般人會想到的汽車駕駛，其實機車的騎士是更該注意安全的。騎機車不像開車一樣外面有一層車殼保護，往往一不注意就造成悲劇。而此海報則是以諷刺的手法呈現騎機車的危險性，不管是未戴安全帽，雙手未握握把，騎車聽音樂，甚至闖紅燈…等等，都是在等待死亡的降臨，連死神坐後座都會戴安全帽了，生命如此寶貴，脆弱的我們更應該珍惜自己的生命！
