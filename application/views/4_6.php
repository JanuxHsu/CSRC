        <dd>
            圖中為表現行車時實是將人身安全暴露於一定的危險環境下，把人體最具代表性亦是最不可或缺的頭部作為標靶的象徵；而四面八方向鏢靶射來的，則是各式駕駛者容易採取的危險行為。一旦失去目標則表示將失去相對應的事物。當然在這些事物當中最珍貴的即是僅有一次的人生，故將其置於標靶的中心。望藉此圖達到警惕行車者容易逾越的行車禮儀，規範；以及進一步告誡，一旦違反了交通規則，可能失去許多寶貴事物。
