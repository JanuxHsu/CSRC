        <dt>
            作品名稱:
        </dt>
        <dd>
            用體貼的心，讓世界更美好-人車相禮讓，生命有保障
        </dd>
        <dt>
            創作說明
        </dt>
        <dd>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每天打開新聞總是會看到好幾則因為交通意外而導致的悲劇，同時每學期上軍訓課的時候也會聽到教官宣導交通安全的重要，還有幾位政大的同學因此去世，令人不勝唏噓‥。我從大二開始騎機車，騎得越久，越發現台北的交通真的存在很多的問題，好幾次我在興隆路上面騎機車時都會有行人無視斑馬線直接橫越馬路，好幾次都逼得我必須緊急煞車。此外當我騎機車在木柵路等紅燈的時候偶而會看到有騎士或是汽車駕駛直接紅燈右轉，我想這對行人來說非常危險的事！因此我這次的作品就是希望能傳達：用體貼的心，讓世界更美好。它的意義是當行人和騎士以及汽車駕駛都擁有一個體貼他人的心互相站在對方的角度著想的話，我相信台北市的交通意外一定可以大幅下降的！這樣會使台北成為更美好的都市。
        </dd>
    </dl>
